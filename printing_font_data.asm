; The table below represents an 8x16 font.  For each 8-bit extended ASCII
; character, the table uses 16 memory locations, each of which contains
; 8 bits (the high 8 bits, for your convenience) marking pixels in the
; line for that character.

; I have described my approach in the comments below but overall, I used a 
; nested loop format to run through rows and columns downwards. While determining 
; the char to be used, I multiplied with 14 by adding the value to itself 4 times
; and I got the MSB which helped me determine what to print. 
; R0: used to print character to screen
; R1: holds address of character to be printed 
; R2: holds the beginning of the font data address
; R3: holds the row counter which repeats from 16 until 0
; R4: holds the column counter which repeats from 8 until 0
; R5: holds the character which needs to be used (value of the address in R2)

			.ORIG x3000;
; CLEAR ALL REGISTERS
			AND R0, R0, #0 ; char
			AND R1, R1, #0 ; address of character to be printed 
			AND R2, R2, #0 ; font_data address
			AND R3, R3, #0 ; row counter
			AND R4, R4, #0 ; column counter
			AND R5, R5, #0 ; font_data value
			AND R6, R6, #0
; SET ROW = 16 TO COUNT DOWN
			ADD R3, R3, #8
			ADD R3, R3, #8
; STORE THE BEGINNING ADDRESS OF THE FONT_DATA
			LEA R2, FONT_DATA
; CALCULATING THE ADDRESS OF THE CHARACTER TO BE PRINTED BY BULTIPLYING BY 16 (ADDING 4 TIMES)
			LDI R1, C_P ; R1 contains the value of the letter
			ADD R1, R1, R1 
			ADD R1, R1, R1 
			ADD R1, R1, R1 
			ADD R1, R1, R1 
			ADD R2, R1, R2 ; new address gets stored into our register
ROWBEGIN		AND R4, R4, #0
			ADD R4, R4, #8 ; setting column to 8 to count down after each outer loop
			LDR R5, R2, #0 ; load value of font_data into R5
COLBEGIN		BRzp FILLZERO ; inner loop begins and we check whether the number is zero or positive (has a leading 0)
FILLONE			LDI R0, C_ONE
			OUT
			BRnzp FINPRINT ; we check whether the number is zero or positive and if yes, we exit to start printing number
FILLZERO		LDI R0, C_ZERO 
			OUT
FINPRINT		ADD R4, R4, #-1 ; col-=1 decrementing inner loop
			BRnz ROWOVER ; if inner loop is over we repeat the outer loop
			ADD R5, R5, R5 ; left shift to get next bit at msb
			BRnzp COLBEGIN
ROWOVER			LD R0, C_NL
			OUT ; print the new line
			ADD R2, R2, #1  ; next char address 
			ADD R3, R3, #-1 ; row-=1 decrementing outer loop
			BRz FIN ; once row is 0 we finished and end
			BRnp ROWBEGIN ; repeat outer loop


C_ZERO			.FILL x5000 ; 0 char stored here
C_ONE			.FILL x5001 ; 1 char stored here
C_P			.FILL x5002 ; print char stored here
C_NL			.FILL x000A ; new line char stored

FIN 			HALT

